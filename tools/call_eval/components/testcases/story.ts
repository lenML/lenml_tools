export const story_testcases = [] as {
  prompt: string;
}[];

function add_one(text: string) {
  const lines = text.split("\n");
  // 去掉开头结尾的空行
  while (lines.length > 0 && lines[0].trim() === "") {
    lines.shift();
  }
  while (lines.length > 0 && lines[lines.length - 1].trim() === "") {
    lines.pop();
  }
  const content = lines.join("\n");
  story_testcases.push({ prompt: content });
}

